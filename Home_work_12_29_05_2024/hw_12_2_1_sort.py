"""
Домашнее задание №8: Сортировка, поиск, регулярные выражения.
1. В супермаркете проводится беспрецедентная акция – «Покупая два любых товара, 
третий получаешь бесплатно*», а внизу мелким шрифтом приписано «* - из трех выбранных вами товаров оплачиваются два наиболее дорогих». Вася, идя в супермаркет, определился, какие товары он хочет купить, и узнал, сколько они стоят. Помогите ему определить минимальную сумму денег, которую ему нужно взять с собой, чтобы в итоге стать счастливым обладателем этих товаров.
Программа получает на вход число N (1≤N≤1000), а затем N чисел – стоимости 
выбранных Васей товаров. Все стоимости – натуральные числа, не превышающие 10000.
Выведите одно число – сумму денег, которую Вася должен взять с собой в супермаркет (минимально возможную).
"""

def minimal_cost(n, prices) -> int:
    """
    Функция для вычисления минимальной суммы денег.

    Args:
    n (int): Количество товаров.
    prices (list[int]): Список стоимости товаров.

    Returns:
    int: Минимальная сумма денег.
    """

    # Сортируем цены в порядке убывания
    prices.sort(reverse=True)

    total_cost = 0

    # Проходим по списку и суммируем стоимости двух наиболее дорогих товаров из каждой тройки
    for i in range(0, n, 3):
        total_cost += prices[i]
        if i + 1 < n:
            total_cost += prices[i + 1]

    return total_cost


if __name__ == "__main__":
    # Пример ввода
    n = 6
    prices = [1, 5, 4, 3, 5, 7]

    # Вычисление минимальной суммы денег
    result = minimal_cost(n, prices)

    # Вывод результата
    print(result)  # Output: 19
