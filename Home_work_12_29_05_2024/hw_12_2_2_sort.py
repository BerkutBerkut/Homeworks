"""
Домашнее задание №8: Сортировка, поиск, регулярные выражения.
2.  Дан список чисел (содержащий не менее двух элементов). Найдите в нем два 
ближайших друг к другу числа (то есть два числа с наименьшей разностью).
Выведите эти числа в порядке не убывания.
Используйте встроенную сортировку языка Python. Решение должно иметь сложность 
встроенной сортировки + O(n).
"""

def find_closest_pair(arr) -> tuple:
    """
    Функция для нахождения двух ближайших чисел в списке.

    Args:
    arr (list[int]): Список целых чисел.

    Returns:
    tuple: Пара чисел с наименьшей разностью.
    """
    # Сортируем список
    arr.sort()

    # Инициализируем минимальную разность и пару чисел
    min_diff = float("inf")
    closest_pair = (None, None)

    # Проходим по отсортированному списку и находим пару с наименьшей разностью
    for i in range(len(arr) - 1):
        diff = arr[i + 1] - arr[i]
        if diff < min_diff:
            min_diff = diff
            closest_pair = (arr[i], arr[i + 1])

    return closest_pair


if __name__ == "__main__":
    # Пример ввода
    arr = [9, 4, 1, 6]

    # Поиск пары чисел с наименьшей разностью
    result = find_closest_pair(arr)

    # Вывод результата
    print(result)  # Output: (4, 6)
